import 'dart:developer' as dev;

class Logger {
  void debug(message) {
    dev.log(message);
  }

  void info(message) {
    dev.log(message);
  }

  void warn(message) {
    dev.log(message);
  }

  void error(message) {
    dev.log(message);
  }

  void fatal(message) {
    dev.log(message);
  }
}
